public class Lec4_Literals {
    public static void main(String[] args) {
        byte age = 26;
        int age2 = 56;
        short age3 = 87;
        long ageDino = 5666666666666L;
        char ch = 'A';
        float f1 = 5.6f;
        double d1 = 4.66;
        boolean a = true;

        System.out.println(age); // Output : 26
        System.out.println(age2); // Output : 56
        System.out.println(age3); // Output : 87
        System.out.println(ageDino); // Output : 5666666666666
        System.out.println(ch); // Output : A
        System.out.println(f1); // Output : 5.6
        System.out.println(d1); // Output : 4.66
        System.out.println(a); // Output : true

        String str = "Nitin";
        System.out.println(str); // Output : Nitin

    }
}
