package practiceProgramsJavaPDF;

public class program23 {
    // Increment and Decrement based examples
    public static void main(String[] args) {
        int a = 1;
        int b = 2;
        int c;
        int d;
        c = ++b; // 3
        d = a++; // 1
        c++; // 4
        System.out.println("a is: " + a); // 2
        System.out.println("b is: " + b); // 3
        System.out.println("c is: " + c); // 4
        System.out.println("d is: " + d); // 1

    }
}
